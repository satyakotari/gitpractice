package www.test.sathya;

public class Multipication 
{
public static void main(String[] args)
{
	int number = 9;
	System.out.println("The multipication table for:"+number);
	for(int i=1; i<=10; i++)
	{
		
		System.out.println(+number+" *"+i+"="+number*i);
	}
	System.out.println("The multipication table for:"  +number+ " is completed");
}
}